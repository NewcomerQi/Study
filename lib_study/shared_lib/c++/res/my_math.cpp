#include "../inc/my_math.h"

int MyMath::my_add(int a, int b)
{
	return a + b;
}
int MyMath::my_sub(int a, int b)
{
	return a - b;
}
int MyMath::my_mul(int a, int b)
{
	return a * b;
}
int MyMath::my_div(int a, int b)
{
	return a / b;
}

